function x=letter2number(a)
switch(a)

    case '-'
         x=1;
    case 'A'    
        x=2;    
    case 'C'    
        x=3;
    case 'D'
        x=4;
    case 'E'  
        x=5;
    case 'F'
        x=6;
    case 'G'  
        x=7;
    case 'H'
        x=8;
    case 'I'  
        x=9;
    case 'K'
        x=10;
    case 'L'  
        x=11;
    case 'M'
        x=12;
    case 'N'  
        x=13;
    case 'P'
        x=14;
    case 'Q'
        x=15;
    case 'R'
        x=16;
    case 'S'  
        x=17;
    case 'T'
        x=18;
    case 'V'
        x=19;
    case 'W'
        x=20;
    case 'Y'
        x=21;
    otherwise
        x=1;
end
end